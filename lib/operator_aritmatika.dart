void main() {
  var first = 10;
  var second = 3;

  print(first + second); // pertambahan
  print(first - second); // pengurangan atau negatif
  print(first * second); // perkalian
  print(first / second); // pembagian (hasil double)
  print(first ~/ second); //pembagian (hasil int)
  print(first % second); // sisa bagi
}
