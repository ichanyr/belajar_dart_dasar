/**
 * Boolean adalah tipe data yang hanya memiliki 2 nilai, yaitu benar atau salah
 * direpresentasikan dengan kata kunci bool
 * untuk nilai benar, menggunakan kata kunci true
 * untuk nilai salah, menggunakan kata kunci false
 */

void main() {
  bool finish = false;
  print(finish);

  finish = true;
  print(finish);
}
